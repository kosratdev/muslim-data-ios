import Foundation

/// List of languages that supported by MuslimData database for translations
///
/// - en: English
/// - ar: Arabic
/// - ckb: Central Kurdish
/// - ckb_BADINI: Kurdish badini dialect
/// - fa: Farsi
/// - ru: Russian
public enum Language: String {
    case en
    case ar
    case ckb
    case ckb_BADINI
    case fa
    case ru
}
